package com.github.isingh78;

public class Operations {

    public static int AddNumber(int i,int j)
    {
        return i+j;
    }

    public static int MultiplyNumber(int i,int j)
    {
        return i*j;
    }

    public static int SubtractNumber(int i,int j)
    {
        return i-j;
    }

}
